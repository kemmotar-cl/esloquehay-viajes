export function getLocalizedTaglines(variant: SpanishVariant): string[] {
  const variantPhrases = VARIANT_PHRASES[variant];
  return [...BASE_TAGLINES, ...variantPhrases];
}

export function getRandomTagline(variant: SpanishVariant = 'es_generic'): string {
  const phrases = getLocalizedTaglines(variant);
  return phrases[Math.floor(Math.random() * phrases.length)];
}

